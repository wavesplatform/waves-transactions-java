package com.wavesplatform.transactions.common;

public class Id extends Base58String {

    public static final int BYTE_LENGTH = 32;

    public Id(byte[] id) {
        super(id);
    }

    public Id(String id) {
        super(id);
    }

    public static Id as(byte[] id) {
        return new Id(id);
    }

    public static Id as(String id) {
        return new Id(id);
    }

    @Override
    public String toString() {
        return encoded();
    }

}
